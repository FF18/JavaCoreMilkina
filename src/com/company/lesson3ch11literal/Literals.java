package com.company.lesson3ch11literal;
// commit for IntelliJ IDEA
public class Literals {

	public static void main(String[] args) {

		char c = 45;
		System.out.println("Literal 10: "+11);
		System.out.println("Literal 8: "+011);
		System.out.println("Literal 16: "+0x12A);
		System.out.println("Literal char: "+'r');
		System.out.println("Literal char: "+c);
		System.out.println("Literal 2: "+0b011);
	}

}
