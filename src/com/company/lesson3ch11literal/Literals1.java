package com.company.lesson3ch11literal;

public class Literals1 {
    public static void main(String[] args) {
        double d1 = 145454545.6767676;
        double d2 = .4; // number 0.4

        double d3 = 234.969263d; // excess
        double d4 = 12234.9263D; // excess

        double d5 = 1.5e14; // volue 1.5*10^14
        double d6 = 1.5E14; // volue 1.5*10^14
        double d7 = 1.5e-14; // volue 1.5*10^-14

        float f1 = 562534762576.454536F;
        float f2 = 78.45f;

       // 1 bayt = 8 bit;
        // 1 kilobayt = 1024 bayt;

        System.out.println("Literals first");
        System.out.println(d1);
        System.out.println(d2);
        System.out.println(d3);
        System.out.println(d4);
        System.out.println(d5);
        System.out.println(d6);
        System.out.println(d7);
        System.out.println(f1);
        System.out.println(f2);
    }
}
