package com.company.lesson4ch1operator;

public class DivisionByModul {

	public static void main(String[] args) {
		int a = 6 % 5;
		double b = 6 % 5;
		double b1 = 6.0 % 5;
		double b2 = 6.0 % 5.0;
		System.out.println(a);
		System.out.println(b); // 1.0
		System.out.println(b1); // 1.0
		System.out.println(b2); // 1.0
	}

}
